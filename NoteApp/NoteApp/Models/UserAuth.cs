namespace NoteApp.Models
{
    /// <summary>
    /// The Model class for Email and Password authorization
    /// </summary>
    public class UserAuth
    {
        /// <summary>
        /// Gets or sets the email.
        /// </summary>
        /// <value>
        /// The email.
        /// </value>
        public string Email { get; set; }

        /// <summary>
        /// Gets or sets the uid.
        /// </summary>
        /// <value>
        /// The uid.
        /// </value>
        public string Uid { get; set; }
    }
}
